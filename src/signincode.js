function SigninPage(){
  
    if (typeof web3 !== 'undefined') {
     
        $('#lgambtn').show();
       
        web3 = new Web3(web3.currentProvider);

        web3.eth.getAccounts().then(function (result) {
            account = result[0];
          
        });
        MyContract = new web3.eth.Contract(abi, address);
        // initiate contract for an address

        myContractInstance = MyContract;



    } else {
       
        web3Provider = new Web3.providers.HttpProvider('http://localhost:7545');
        web3 = new Web3(App.web3Provider);

        MyContract = new web3.eth.Contract(abi, address);
        // initiate contract for an address

        myContractInstance = MyContract;
    }

}




$(document).ready(function(){


        

  $('#lgmbtn').click(function () {
    var add = $('[name="ethaddress"]').val();
    if (add > 0) {
        try {
            myContractInstance.methods.allmembers(add).call().then(function (result) {
                
                if (result[0]) {
  
                   
  
                        localStorage.setItem("uint", add);
                        location.href = "dashboard.html";
                    }
                    else {
                        $('.error').html('You need to register first');
                        $('.error').show();
                    }
                
  
            });
  
        }
        catch (err) {
            console.log(err);
        }
  
    }
    else {
        $('.error').html('Enter Valid ETH address');
        $('.error').show();
    }
  });
    
});